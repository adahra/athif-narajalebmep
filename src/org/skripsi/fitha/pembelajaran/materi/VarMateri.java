package org.skripsi.fitha.pembelajaran.materi;

/**
 * 
 * @author blackshadow
 *
 */
public class VarMateri {
	private int idBab;
	private int idSubBab;
	private String subJudul;
	private String isiMateri;
	private String gambar;
	private String suara;
	private String kurikulum;

	/**
	 * @return the idSubBab
	 */
	public int getIdSubBab() {
		return idSubBab;
	}

	/**
	 * @param idSubBab
	 *            the idSubBab to set
	 */
	public void setIdSubBab(int idSubBab) {
		this.idSubBab = idSubBab;
	}

	/**
	 * @return the idBab
	 */
	public int getIdBab() {
		return idBab;
	}

	/**
	 * @param idBab
	 *            the idBab to set
	 */
	public void setIdBab(int idBab) {
		this.idBab = idBab;
	}

	/**
	 * @return the subJudul
	 */
	public String getSubJudul() {
		return subJudul;
	}

	/**
	 * @param subJudul
	 *            the subJudul to set
	 */
	public void setSubJudul(String subJudul) {
		this.subJudul = subJudul;
	}

	/**
	 * @return the isiMateri
	 */
	public String getIsiMateri() {
		return isiMateri;
	}

	/**
	 * @param isiMateri
	 *            the isiMateri to set
	 */
	public void setIsiMateri(String isiMateri) {
		this.isiMateri = isiMateri;
	}

	/**
	 * @return the gambar
	 */
	public String getGambar() {
		return gambar;
	}

	/**
	 * @param gambar
	 *            the gambar to set
	 */
	public void setGambar(String gambar) {
		this.gambar = gambar;
	}

	/**
	 * @return the suara
	 */
	public String getSuara() {
		return suara;
	}

	/**
	 * @param suara
	 *            the suara to set
	 */
	public void setSuara(String suara) {
		this.suara = suara;
	}

	/**
	 * @return the kurikulum
	 */
	public String getKurikulum() {
		return kurikulum;
	}

	/**
	 * @param kurikulum
	 *            the kurikulum to set
	 */
	public void setKurikulum(String kurikulum) {
		this.kurikulum = kurikulum;
	}
}
